package Einfuehrung;

public class _3_Benennung {
	public static void main(String[] args) {
		/*
		 * Benennung von Klassen, Methoden (Funktionen), Variablen und Konstanten
		 * 
		 * Java-Vorgaben: Bezeichner (Identifier) dürfen nur A-Z, a-z, 0-9, _ (Unterstrich) und $ (Dollar-Zeichen) enthalten
		 * 				  Bezeichner dürfen nicht mit einer Ziffer starten
		 * 				  Bezeichner können beliebig lang sein
		 * 				  Bezeichner unterscheiden sich in Gross- und Kleinschreibung
		 * 				  Bezeichner dürfen kein Schlüsselwort sein
		 * 
		 * Vorgaben der Entwickler:  Klassen-Namen starten stets mit einem Grossbuchstaben und verwenden CamelCase
		 * 						Bsp: public class KlassenBezeichner {
		 * 							 Methoden-Namen starten stets mit einem Kleinbuchstaben und verwenden CamelCase
		 * 						Bsp: public void methodenBezeichner() {
		 * 							 Variablen-Namen starten stets mit einem Kleinbuchstaben und verwenden CamelCase
		 * 						Bsp: int variablenBezeichner = 12;
		 * 							 Konstanten-Namen bestehen aus Grossbuchstaben, wobei Wörter mit einem Unterstrich getrennt werden
		 * 						Bsp: double KREISZAHL_PI = 3.14159265359;
		 * 
		 * Es sollten immer aussagekräftige Bezeichner gewählt und auf Abkürzungen verzichtet werden.
		 * So geht besser hervor welchem Zweck die Variable dient.
		 */
	}
}
