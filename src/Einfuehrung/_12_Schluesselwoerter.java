package Einfuehrung;

public class _12_Schluesselwoerter {
/*
 * 	Das sind nur einige wenige Schlüsselworte, die aber wichtig sind
 * 	Schlüsselworte die wir bereits anderweitig kennengelernt haben (bspw. int oder for) sind hier auch ausgenommen
 * 
 * 	public		- die Variable, Methode oder Klasse ist im gesamten Projekt sichtbar, jeder kann darauf zugreifen
 * 	private		- die Variable oder Methode ist nur in der eigenen Klasse sichtbar, außerhalb dieser kann niemand darauf zugreifen
 * 	protected	- die Variable oder Methode ist im gesamten Projekt sichtbar, außer in nicht-erbenden Klassen aus einem anderen package
 * 	default		- die Variable oder Methode ist nur im eigenen Package sichtbar
 * 				- sollte eine Variable oder Methode default sein, so wird KEIN Modifier davor geschrieben - das Schlüsselwort default steht für etwas anderes (siehe Switches)
 * 	final		- die Variable oder der Parameter ist festgelegt und kann nicht mehr verändert werden 
 * 				- die Methode ist festgelegt und kann durch eine andere Methode mit @Override nicht überschrieben werden
 * 				- die Klasse ist festgelegt, von ihr kann nicht geerbt werden
 * 	@Override	- die nachfolgende Methode überschreibt andere Methoden mit selbem Namen, selben Modifizieren und selben Übergabewerten (alles muss gleich sein)
 * 	break		- die Schleife wird abgebrochen - der nachfolgende Code innerhalb der Schleife wird nicht mehr ausgeführt
 * 	continue	- die Schleife startet einen neuen durchlauf - der nachfolgende Code innerhalb der Schleife wird in diesem Durchlauf nicht mehr ausgeführt
 * 	new			- erzeugt ein neues Objekt
 * 	import		- importiert andere Klassen, so dass sie (und ihre Methoden) in dieser Klasse genutzt werden können
 * 	package		- gibt die Paketzugehörigkeit der Klasse an
 * 
 * eine vollständige Liste der Schlüsselworte gibt es hier: https://de.wikibooks.org/wiki/Java_Standard:_Schlüsselwörter
 */
}
